#include <stdio.h>
#include <stdlib.h>

int main()
{
    float x, y;
    float sum;
    float sub;
    float  mult;
    float    divd;
    scanf( "%f %f",&x,&y );
    x+y;
    sum = x+y;
    printf("sum=%f  \n ",sum  );


    x-y;
    sub= x-y;
    printf("sub=%f \n ",sub  );


    x*y;
    mult = x*y;
    printf("mult=%f \n ",mult );


    x/y;
    divd = x/y;
    printf("divd=%f  \n ",divd );

    return 0;
}
